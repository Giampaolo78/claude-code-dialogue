#!/usr/bin/env bash
# upgrade.sh - refresh the dialogue across ALL attached projects.
#
#   dlg upgrade            -> git pull the engine, then re-attach every registered project
#                             (so their per-project /dialogue-* command copies pick up the update).
#   upgrade.sh --no-pull   -> re-attach only (used by the post-merge git hook, which runs right
#                             AFTER a pull -- pulling again would be redundant).
#
# WHY this exists: a `git pull` updates the engine + the ALFA hooks (referenced by absolute path,
# auto-applied), but NOT the per-project command copies (generated by attach.sh at attach time).
# This regenerates them everywhere in one shot. Re-attach is DATA-SAFE: it only rewrites the
# command copies + COORDINATION.md + the project-name file; the registry, boards and cursors are
# create-if-missing, never reset (verified).
#
# INVARIANT: this MUST NOT fail the caller (the post-merge hook must never break `git pull`).
# So: no `set -e`, every step tolerates errors, and we always exit 0.
set -u

SELF="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REGISTRY="${DIALOGUE_REGISTRY:-$SELF/.attached_projects}"
NOPULL=0
[ "${1:-}" = "--no-pull" ] && NOPULL=1

# 1) pull the engine (resolve to the real repo: $SELF may be reached via the ~/.claude-code-dialogue symlink)
if [ "$NOPULL" = "0" ]; then
  top="$(git -C "$SELF" rev-parse --show-toplevel 2>/dev/null || true)"
  if [ -n "$top" ]; then
    echo "[upgrade] git pull in $top"
    git -C "$top" pull --ff-only 2>&1 | sed 's/^/  /' || echo "[upgrade] WARN: git pull failed (continuing with re-attach)"
  else
    echo "[upgrade] note: engine is not a git checkout -> nothing to pull"
  fi
fi

# 2) re-attach every registered project; PRUNE the ones whose dir is gone.
if [ ! -f "$REGISTRY" ]; then
  echo "[upgrade] no attached projects registered yet (nothing to refresh)."
  exit 0
fi

# snapshot the registry: attach.sh re-appends during the loop; we read the snapshot and rewrite
# the registry from the survivors at the end (no read-while-append race).
snap="$(mktemp 2>/dev/null)" || snap="/tmp/dlg_upg_$$"
cp "$REGISTRY" "$snap" 2>/dev/null
keep="$(mktemp 2>/dev/null)" || keep="/tmp/dlg_keep_$$"
: > "$keep"
n=0; pruned=0; failed=0
while IFS= read -r p; do
  [ -z "$p" ] && continue
  if [ ! -d "$p/.dialogue" ]; then
    echo "[upgrade] pruned (project gone): $p"
    pruned=$((pruned + 1))
    continue
  fi
  if "$SELF/attach.sh" "$p" >/dev/null 2>&1; then
    echo "[upgrade] refreshed: $p"
    n=$((n + 1))
  else
    echo "[upgrade] WARN: re-attach failed (kept in registry, e.g. no permission): $p"
    failed=$((failed + 1))
  fi
  printf '%s\n' "$p" >> "$keep"
done < "$snap"

# finding B: preserve any project a concurrent `dlg attach` appended AFTER our snapshot. Re-read the
# live registry and keep entries that were NOT in the snapshot (a PRUNED entry WAS in the snapshot,
# so it is not re-added -> prune still wins). Then rewrite from the survivors (dedup, unique tmp).
if [ -f "$REGISTRY" ]; then
  while IFS= read -r q; do
    [ -z "$q" ] && continue
    grep -qxF "$q" "$snap" 2>/dev/null || printf '%s\n' "$q" >> "$keep"
  done < "$REGISTRY"
fi
rt="$REGISTRY.$$.tmp"
sort -u "$keep" > "$rt" 2>/dev/null && mv -f "$rt" "$REGISTRY" 2>/dev/null
rm -f "$snap" "$keep" 2>/dev/null
echo "[upgrade] refreshed $n project(s), pruned $pruned, failed $failed."
exit 0
